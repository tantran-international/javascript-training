// primary expression
const a = 1.999;
console.log(a);
console.log('something');
console.log(true);

// arithmetic expressions
const b = 1 + 1;
const c = a + 0.001;
console.log(b);
console.log(c);
console.log(b / c);
console.log(56 % 3);

// string expressions
const x = 'Hello ';
const y = 'World!';
console.log(x);
console.log(x + y);

// logical expressions
console.log(b === c);
console.log(b && c == 2);
console.log(b && c != 2);
console.log((a == 2) || (c == 2));
