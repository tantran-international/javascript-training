/* Primitive types */
// Number
const a = 1.5;
const b = 10 * 20;
console.log(a);
console.log(b);

// String
const c = 'Hello World';
console.log(c);

// Boolean
const d = true;
console.log(d);

// sym1 is a symbol with the description 123
const sym1 = Symbol(123);
console.log(sym1);
console.log(typeof(sym1));

// intentionally left blank and pointed to a blank value
const x = null;
console.log(x);

// declared but not initialized value
let y = undefined;
y = 1;
console.log(y);
